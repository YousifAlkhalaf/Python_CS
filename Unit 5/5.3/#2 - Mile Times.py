def walk_time(miles, speed = 20):
    return miles * speed

print(f'The walk time is {walk_time(3)} minutes')
print(f'The walk time is {walk_time(5, 10)} minutes')
print(f'The walk time is {walk_time(1.5, 18)} minutes')
print(f'The walk time is {walk_time(2, 14)} minutes')